public class Youtuber {

    private String nombreCanal;
    private String categoria;
    private int numSubs;

    public String getNombreCanal() {
        return nombreCanal;
    }

    public void setNombreCanal(String nombreCanalEntrada) {
        nombreCanal = nombreCanalEntrada;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoriaEntrada) {
        categoria = categoriaEntrada;
    }

    public int getNumSubs() {
        return numSubs;
    }

    public void setNumSubs(int numSubsEntrada) {
        numSubs = numSubsEntrada;
    }
}
